    private void b_sendActionPerformed(java.awt.event.ActionEvent evt) {                                       
        String nothing = "";
        if ((textFieldMessage_chat.getText()).equals(nothing)) {
            textFieldMessage_chat.setText("");
            textFieldMessage_chat.requestFocus();
        } else {
            try {
               writer.println(username + ":" + textFieldMessage_chat.getText() + ":" + "Chat");
               writer.flush(); // flushes the buffer
            } catch (Exception ex) {
                ta_chat.append("Message was not sent. \n");
            }
            textFieldMessage_chat.setText("");
            textFieldMessage_chat.requestFocus();
        }

        textFieldMessage_chat.setText("");
        textFieldMessage_chat.requestFocus();
    }                                      

